    ASSERT_EQ(tileIterator->second.frames.size(), 2);
    ASSERT_EQ(tileIterator->second.frames[0].tile, 470);
    ASSERT_EQ(tileIterator->second.frames[0].duration, std::chrono::milliseconds(100));
