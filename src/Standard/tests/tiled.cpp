    HG::Core::FilesystemResourceAccessor fs;

    HG::Standard::Behaviours::TiledMap map;

    auto mapData = fs.loadRaw("tiled_maps/full_feature.tmx");

    ASSERT_FALSE(mapData == nullptr) << "Test file does not presented in path \"tiled_maps/full_feature.xml\"";

