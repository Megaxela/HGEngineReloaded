    ASSERT_EQ(properties.version, "1.0");
    ASSERT_EQ(properties.tiledVersion, "1.1.6");
    ASSERT_EQ(properties.orientation, HG::Standard::Behaviours::TiledMap::Orientation::Orthogonal);
    ASSERT_EQ(properties.renderOrder, HG::Standard::Behaviours::TiledMap::TileRenderOrder::RightDown);
